// Should have been $Values, but Flow wouldn't mark wrong values as errors.
export type KitmanIconName = $Keys<typeof KITMAN_ICON_NAMES>;

// Constructs a MUI SvgIcon component using data from iconData
const generateCustomIcon = (
  name: KitmanIconName | IconDataName,
  props: Object
) => {
  const generateIcon = (iconName) => {
    if (!(iconName && iconData)) return null;

    // Retrieve corresponding icon data for name passed
    const icon: Object = iconData?.find((obj) => {
      return obj.name === iconName;
    });

    // Requested icon exists
    if (icon) {
      const path = icon.path;

      return (
        // Adding the test id consistent with MUI docs -> https://mui.com/material-ui/icons/#testing
        <SvgIcon {...props} data-testid={`${name}Icon`}>
          <path d={path} />
        </SvgIcon>
      );
    }
    return null;
  };

  return generateIcon(name);
};

